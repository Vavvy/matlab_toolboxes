% $$(1, 1, 0, 0, 0) \cdot \vec{x}_i \leq 5$$
%
% $$\left[ 
% { \matrix{    -1 & 0 &  1 &    1 &  1 \cr 
%               -1 & 1 &  1 &  0.5 & -1 \cr
%                2 & 1 &  1 & -0.5 &  2 \cr
%                2 & 1 & -1 & -0.5 &  2 \cr } } 
% \right] \cdot \vec{x}_i = (3, 2.5, 5, 3)^T$$
%
numerics.conSetOfPoints(5, 25, [ 1, 1, 0, 0, 0], 5, ...
                                   [-1, 0, 1, 1, 1; ...
                                    -1, 1, 1, 0.5, -1; ...
                                    2, 1, 1, -0.5, 2; ...
                                    2, 1, -1, -0.5, 2], [3; 2.5; 5; 3], ...
                                   [0,1,0,1,0], [5,5,3,4,2]);

%%

close all;
clear all;
 
%%

plotnonlcon= 0; % 1 - takes very long

