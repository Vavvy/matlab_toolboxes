%% Syntax
%       toolboxID= <<toolbox_id/>>.getToolboxID()
%
%% Description
% |toolboxID= <<toolbox_id/>>.getToolboxID()| returns the ID of the
% _<<toolbox_name/>>_ Toolbox. Each toolbox must have a unique ID.
%
%%
% @return |toolboxID| : <matlab:doc('char') char> containing the
% ID of the _<<toolbox_name/>>_ Toolbox. 
